public class Calculador {
    
    float operador1;
    float operador2;
    float resultado;
    
    void sumar(){
        
        resultado=operador1+operador2;
                
    }

    void restar() {
        
        resultado=operador1-operador2;
        
    }
    
    void dividir(){
        
        resultado=operador1/operador2;
        
        
    }
    void multiplicar(){
    
    resultado=operador1*operador2;
    }
    
    
} 
